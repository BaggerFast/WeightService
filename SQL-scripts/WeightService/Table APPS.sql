-- Table APPS
----------------------------------------------------------------------------------------------------
declare @insert bit = 0
declare @app nvarchar(32) = N'TEST_APP'
----------------------------------------------------------------------------------------------------
if (@insert = 1) begin
	if not exists (select 1 from [db_scales].[APPS] where [NAME]=@app) begin
		insert into [db_scales].[APPS]([NAME]) values(@app)
	end
end
----------------------------------------------------------------------------------------------------
select [UID]
from [db_scales].[APPS]
where [NAME]=@app
----------------------------------------------------------------------------------------------------
select [UID], [NAME]
from [db_scales].[APPS]
order by [NAME]
----------------------------------------------------------------------------------------------------
